    <b>2.	Rules and Regulations</b><br>


    2.1	The Track and Field competition of the 13th Sri Lanka University Games - 2019 will be organized in accordance with the most recent technical regulations of the IAAF as adopted by the Athletic Association of Sri Lanka (AASL) and Sri Lanka Universities Sports Association (SLUSA).
   <br> 2.2	The program and event schedule will be fixed by the organizing committee and it will only be changed due to weather conditions or committee decisions.
    <br>2.3	Each University is authorized to enter two competitors and one substitute for each event (men & women).
   <br> 2.4	An individual athlete can enter for only three individual events.
   <br> 2.5	Each University may enter one team in each of the relay events. Any four athletes
    from among those entered for the competition, whether for that or any other event may be
    used in the composition of the relay team for any round. Once a relay team has stared, only
    two additional athletes may be used as substitutes in the composition of the team.<br>

    2.6	The composition of a team and the order of running for a relay shall be officially
    declared one (01) hour prior to the commencement of the event.<br>
    2.7	Competitors' clothing and Shoes must be in accordance with IAAF Rule 143.<br>
    2.8	Every competitor should be provided with two number bibs which, during the
    competition shall be worn visibility on the Breast and Back, except in the Jumping Events,
    where one number bib may be worn on the breast or back only.<br>
    2.9	Protests<br>
    Protests concerning the status of a competitor to participate in a
    competition must be made, prior to the commencement of such competition,
    to the technical committee. Once the technical committee make a decision,
    there shall be right of appeal to jury of appeal. If the matter cannot be
    satisfactorily solved prior to the competition, the competitor shall be
    allowed to complete 'Under protest' and the matter be referred to the SLUSA authority.<br>
    2.9.1	Protest concerns the result or conduct of an event, in the first instance,
    be made orally to the Referee by the athlete or by an official representative of a team.<br>
    2.9.2	Once the Referee make a decision, there shall be right of appeal to jury of appeal.
    An appeal to the Jury of Appeal must be made within 30 minutes:<br>
    a)	If the official announcement of the decision made by the Referee,
    amended result of an event arising from the decision made by the Referee; or<br>
    b)	If the advice being given to those making the protest, where there is no amendment
    of any result.<br>
    c)	It shall be in writing, signed by a responsible official on behalf of the
    athlete or team, and shall be accompanied by a deposit of Rs.500/=. The protest
    fee will be forfeited if the protest is rejected or will be refund in the event
    of it being upheld.<br>
    2.10	 The team declaration forms duly filled must be handed over to the meet
    secretary before 3.00 pm. On 26th of July 2019. The above declaration should consist
    two competitors per individual event from the original entry. No alteration
    is permitted except medical reason which must be verified by a medical officer
    appointed by the Organizing Committee, may be made only until one hour prior to
    the commencement the particular event.<br>
    2.11	Official accreditation card of the competitor should be produced
    at the call room and in the competition area if necessary.<br>
    2.12	Any competitor exclude from participation in all further events if he or she,<br>
    i)	After final confirmation failed to participate<br>
    ii)	Qualified in preliminaries but failed to participate further or
    failed to participate honestly (Medical certificate from a medical officer
    appointed by the Organizing Committee or justifiable reasons may accept by the
    Organizing Committee)<br>

    Call time: before the official commencement of the event<br>
