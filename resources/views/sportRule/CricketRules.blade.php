    <div class="col-md-4"> 
    
    <h4 style="color:#dae44d;font-weight:bold;">SEMIFINALS</h4>
If the semifinal is abandoned and if no result is achieved under Parabola system, then the winner will be decided as follows:
    <ol type="a" style="color:black;">
<li>If both the teams have played any of their first round matches, then the team that finished in the higher position in the Group stage
    will proceed to finals.</li>
    <li>If still equal, then the team with the higher net run rate in all matches (both Group stage and quarter-final) will proceed to finals.</li>
    <li>If a team hasn't played a single first round game or no result is achieved in all their first round matches, then the net run
        rate in the quarter finals of both the teams will be considered.</li>
        <li>In the Event is the tied Semi Final, the teams shall Compete in a super over to determine which team is the winner (Refer attached Appendix-05)</li>
    </ol>
