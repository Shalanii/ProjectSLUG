        <h4 style="color:#dae44d; font-weight:bold;">QUARTER-FINALS</h4>
If the quarter final is abandoned and if no result is achieved under Parabola system, then the team that finished in the higher position in the
    Group stage shall proceed to the semi-finals. However, in a situation where a team qualifies to quarter final stage and no result
    is achieved in their all first round matches, the other team will proceed to semifinal stage given they have played any of their first round matches.<br>

    <ol type="a" style="color:black;">
        <li>In the Event is the tied Quarter Final, the teams shall Compete in a super over to determine which team is the winner (Refer attached Appendix-05)</li>
