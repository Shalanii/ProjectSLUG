    02.	Rules & Regulation<br>

    2.1 The competition will be conducted in accordance with
    the rule and regulation of the World Tennis Association, Sri Lanka
    Tennis Association and Tournament rules laid down by Sri Lanka
    Universities Sports Association.<br>
    2.2 Universities may register both men and women teams for the Championship.
    Composition and number are the specified below.<br>

