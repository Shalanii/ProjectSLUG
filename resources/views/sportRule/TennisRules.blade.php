    2.3 A match will be composed of three single event
    and Two Double match events. A team is considered as the winner
    of the match when that team wins 3 events out of five. A match
    shall commence with singles and if no decision, followed by double.<br>
    2.4 Only Students who are full time internally registered undergraduate
    students of a recognized university are eligible to take part
    in the championship.<br>
    2.5 A competitor can play only in one single and a double match.
    Competitor who had played in single events shall not be permitted
    to combine together to play a double.<br>
    2.6 The captain should hand over the list of names according
    to ranking order of playing to the organizing secretary/tournament
    coordinator 15 minutes prior to the commencement of the tournament.<br>
    2.7   Preliminary rounds will be decided by nine point games.<br>
    2.8    Semifinals, Consolation finals and final matches will be decided
    by best of three sets.<br>
    2.9<br>
    a) The following points shall be taken in to decide the ranking in
    preliminaries.
