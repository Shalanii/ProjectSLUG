    3.1.	Ippon is awarded for: Jodan kicks, throwing or leg
    sweeping the opponent to the mat, followed by a scoring technique.<br>
    3.2.	Waza-ari is awarded for: Chudan kicks, punches on the back
    combination hand technique, unbalancing the opponent and the scoring.<br>
    3.3	 Yuko is awarded for: Chudan/jodan/zukies and uchies<br>
    3.4	 A score is awarded when a technique is performed according
    to the following criteria to a scoring area.<br>
    <ul>
        <li>Good form</li>
        <li>Awareness (zanshin)</li>
        <li>Sporting attitude</li>
        <li>Good Timing</li>
        <li>Vigorous application</li>
        <li>Correct distance</li>
    </ul><br>
