    2.4 Event description<br>
    <ul>
        <li>Individual Kata event will be conducted as an open event. Each University
            is authorized to enter [2] participants and one substitutes
            for the Individual Kata event.</li>
        <li>Individual Kumite event will be conducted under weight categories.</li>
        <li>For the team Kata events each team must have three (03) participants
            and may enter [02] substitutes. This will be conducted as open
            event therefore; weight classes will not be applied.</li>
        <li>Each university can have only one team for each team event.</li>
        <li>For individual events applicants should fill the individual entry
            form. Maximum of [02] competitors could be entered for each weight
            category and only one competitor could be participated for
            the competition.</li>
        <li>Final declaration form should be submitted at the Technical
            Committee Meeting.</li>
        <li>For team events, the details of the team members
            of each university should specify in the team form.</li>
        <li>If the participation of an individual Kumite event is less
            than five (05) the events will be associated.</li>
        <li>Tournament will be officiated according to the rules of
            Sri Lanka Karate- Do federation.</li>
    </ul>

    2.5 The Conduct of the Tournament<br>
    Kata Competition:

    <ol type="a">
        <li>Individual	-  Shitei or tokui (free) –WKF allowed Kata</li>
        <li>Team	- Bunkai is not allowed</li>
        <li>Kata performing in round cannot be repeated.</li>
        <li>Point system will be applied to select the best 5 teams
            for 2nd round.</li>
    </ol>
    Note: In Kata Competition, scoring is based upon on overall evaluation of a number of elements including accuracy, spiritual and mental vigor, dynamism of quickness and calmness and others.

    <br>
    Kumite Competition:
    <ol type="a">
        <li>Male	-  All matches two (02) minutes</li>
        <li>Female	- All Matches two (02)minutes</li>
    </ol>
    Note: Attacks are limited to the following areas: Head, face,
    neck, abdomen, chest, back sides.<br><br>
