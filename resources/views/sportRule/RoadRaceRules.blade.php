    3.	System of Competition<br>
    The Championship will be conducted on the basis of team aggregate,
    by adding the respective places of each competitor. The team having the
    lowest aggregate shall be declared the winner. In an event of a tie,
    it shall be resolved in favour of the team whose last scoring member
    finishes the closer to the first place.<br><br>

    4.	Team Declaration<br>
    A team declaration certified by the Director of Physical
    Education should be handed over to the Director Competitions prior to
    the Technical Committee Meeting by the team captain. All competitors
    should report to the call room at 5.30 a.m. 7th of September 2019<br><br>

    5.	Medical Reports<br>
    All competitors must bring a Medical report issued by the Chief Medical
    Officer of the University and should be submitted at the Technical Committee
    Meeting. If needed every participant responsible to produce medical
    certificate which issued from Sport Medicine Doctor.<br><br>

