    2.4	If two or more teams obtain the same points the ranking shall be decided by the
    quotient of the sets won and lost for the whole matches.<br>
    2.5	If they again score equal, the ranking will be decided by the quotient of the
    whole points won and lost.<br>
    2.6	If two teams are still equal, priority shall be given to a team according
    to the results of the match played between the two concerned.<br>
    2.7	If a team declares its forfeit  during a match or tournament it will be
    expanded from the championship and all results of matches it already played during the
    respective  round will be nullified the respective opponent teams will be declared winner
    of the respective matches. Opponent teams will be declared winner of the respective
    matches.<br><br>


    3.	Reporting Time<br>
    3.1	The teams should report at the registration table 30 minutes
    ahead of commencement of their respective matches. Any team not present even
    after 15 minutes from their scheduled time for the match will be considered to
    have given a WALK - OVER to their opponents.<br>
    3.2	Each team should wear their team colors while playing. But the Libero
    player should wear the different colored jersey and/or short.<br>
    3.3	Each player should wear a clear number to identify herself/himself.
    (One on the center of the chest and the other on the center of the back of the
    playing jersey.) The height of the front number is 15 cm & back number is 20 cm.<br>
    3.4	Each team is allowed wear number from 1-18 only.
    Each team should bring with them their own volleyballs for the practices.<br><br>

