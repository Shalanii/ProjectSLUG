    1.	Date & Venues<br>

    3rd, 4th, 5th& 6th September 2019<br>
    Indoor Stadium, Kotawila<br><br>

    2.	Rules & Regulation<br>
    2.1	The Championship will be conducted in accordance with the new Rules and
    Regulations of the International Volleyball Federation (FIVB) which are adopted,
    by the Sri Lanka Volleyball Federation (SLVF) and the tournament rules laid down by the
    SLUSA.<br>

    2.2	Universities may register both men and women teams for the Championship.
    Composition and numbers are as specified below.<br>
    <ol type="a">
        <li>One Manager</li>
        <li>One Chaperon (Only for the women's team)</li>
        <li>One Head Coach</li>
        <li>One Assistant Coach</li>
        <li>One Trainer</li>
        <li>Twelve Players</li>
    </ol>
