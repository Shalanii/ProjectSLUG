     2.4	Following terminologies are being used for the championships. <br>
    <ul>
        <li>A match – competition between two universities</li>
        <li>A event – a single or a double event of a match</li>
    </ul>

    2.5	A Match will be composed of three single events and two double events. It shall
    commence with single events and if no decision, followed by double events. A team is considered
    as the winner of the tie when that team won three events.<br>
    2.6	A Competitor can play only in one single event and one double event of a particular match.
    Competitors who had played in single events shall not be permitted to combine together to play in a double event of the same match.<br>
    2.7	The team/ competitor(s) should report at the registration table 20 minutes prior to the commencement
    of their respective match/ event. Any team/ competitor(s) does not present match/event after 10 minutes from their scheduled time for the
    match/event will be considered to have given a WALK- OVER to their opponents.<br>
    2.8	Any team would not able to withdraw during the championship without proper reason.<br>
    2.9	The captain should hand over the team declaration form certified by the team manager to the organizing secretary/Tournament
    Coordinator 20 minutes prior to the commencement of the match. The declaration should consist the order of play and no
    further alterations will be permitted.<br>
    2.10	In singles/Doubles a match will be decided by best of five sets with a set of eleven (11) points.<br>
    2.11	First round matches of group stage will be in league basis.<br>
    2.12	The following system will be adopted to select teams for the quarter finals.<br><br>

    a)	The following points shall be taken in to account to decide the ranking in preliminaries.<br>


