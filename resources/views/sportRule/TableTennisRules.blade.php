    b)	If two teams equal at any ranking stage, the winner of their match at group stage
    will be ranked higher.<br>
    c)	If three or more teams obtained the same points, their relative rank shall be
    determined by only considering results of the matches between them.<br>

    <ol type="i">
        <li>Ranking will be established by the quotient of total events (Singles/Doubles)
            won and total events (Singles/Doubles) lost, with greater quotient ranked higher.
            After that if still two teams equal, their relative ranks will be decided according
            to rule 2.12 (b).</li>

        <li>If three or more teams obtained equal quotient of total events won and total
            events lost, ranking will be established by the quotient of total sets won and total
            sets lost, with greater quotient ranked higher. After that if still two teams equal,
            their relative ranks will be decided according to rule 2.12 (b).</li>

        <li>If three or more teams are equal quotient of total events won and total events
            lost and are also equal quotient of total sets won and total sets lost, ranking
            will be established by the quotient of total points won and total points lost,
            with greater quotient ranked higher. After that if still two teams equal, their
            relative ranks will be decided according to rule 2.12(b).</li>

        <li>If three or more teams are still equal, then ranking will be established by drawing lots.</li>

        <li>If a competitor retired during an event due to an injury or a medical reason,
            points will be added to the opponent’s score while keeping the current score of the
            retired competitor to decide ranking in the group.</li>
    </ol>
        2.13	The Quarter Finals, Semi - Finals, Losers Final and Final will be worked off
    according to system laid down by the SLUSA.<br>
    2.14	Note: All Universities should bring 3 plastic balls (ITTF approved three star 40+) per team.<br>
