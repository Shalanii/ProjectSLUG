<div class="list-group">
    <li class="list-group-item bg-primary" style="font-weight:bold">2.	Rules & Regulations</li>
    <li class="list-group-item">2.1	The tournament will be organized according to the Swiss system for teams with five rounds.
    The technical rules of FIDE (The World Chess Federation) will be applicable in absence of any
    information given before. If the participation is reduced to less than six teams the Round -Robin system
    will be used. The match points system will be used in ranking the teams</li>
    <li class="list-group-item">2.2	Each team should comprise of six players and two reserves. The team order,
    which should be fixed during the whole event, should be submitted to the Chief arbiter before 15 minutes of the starting time of the first day.</li>
    <li class="list-group-item"> 2.3	Board orders should also be submitted to the tournament arbiter before 15 minutes of the starting time of each round. In absence of
    doing so, the tournament arbiter will consider that first six players are playing in the round. The reserves can play only
    in last two boards as the given orders.</li>
</div>
<br>
<div class="list-group">
    <li class="list-group-item bg-primary" style="font-weight:bold">3.	  Chess Clock</li>
    <li class="list-group-item">The Fisher mode will be used giving Chess Clocks for each and every Game. (90 minutes each + increment of 30 Seconds per move)</li>
</div>
<br>
<div class="list-group">
    <li class="list-group-item bg-primary" style="font-weight:bold">4.	Walk over</li>
    <li class="list-group-item">Walk over will be conceded by those who arrive more than 30 minutes late from the starting time.</li>
</div>
<br>
<div class="list-group">
    <li class="list-group-item bg-primary" style="font-weight:bold">5.	  Scoring</li>
    <li class="list-group-item">One point shall be awarded to a winning game, half for a draw and zero for a loss. If a team scores more than 3 points,
    it is considered as won the match. If it is 3 points each, it is considered as a draw. A match winner will be given 2 match points,
    a draw 1 match point and the loser 0. The total number of Match points will be taken for the final ranking.</li>
</div>
<br>
<div class="list-group">
    <li class="list-group-item bg-primary" style="font-weight:bold">6.	  Winner</li>
    <li class="list-group-item">There are maximum of 10 match points for 5 rounds in the tournament. The team which scores the highest
    number of match points out of the possible 10 is awarded the championship. The team which scores the second (2nd)
    highest number of match points will be awarded the second (2nd) place. This procedure will be used till the last place.</li>
</div>
<br>
<div class="list-group">
    <li class="list-group-item bg-primary" style="font-weight:bold">7.	  Ties</li>
    <li class="list-group-item">If there are two or more teams tied for any position, the Ties will be broken using the following tie breakers:
	<ul class="list-group">
		    <li class="list-group-item">The total of Game Points</li>
        	<li class="list-group-item">Percentage SB</li>
        	<li class="list-group-item">Individual result between tied teams.</li>
        	<li class="list-group-item">Results of players starting from board 1</li>
	</ul>
	</li>
</div>
